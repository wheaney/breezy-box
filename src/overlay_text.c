/* ----------------------------------------------------------------
 * Text → RGBA pixel buffer
 * ---------------------------------------------------------------- */

/*
 * Measure how many columns the text occupies (handles newlines).
 * Returns the widest line in characters.
 */
static int measure_cols(const char *text)
{
    int max_col = 0;
    int col = 0;

    for (; *text; text++) {
        if (*text == '\n') {
            if (col > max_col)
                max_col = col;
            col = 0;
        } else {
            col++;
        }
    }
    if (col > max_col)
        max_col = col;
    return max_col;
}

static int count_lines(const char *text)
{
    int lines = 1;

    for (; *text; text++)
        if (*text == '\n')
            lines++;
    return lines;
}

/*
 * Render `text` into a freshly-malloc'd RGBA buffer.
 * *out_w and *out_h receive the pixel dimensions (before scaling).
 * The caller is responsible for free()ing the returned buffer.
 * Text colour: white (255,255,255) on transparent black background.
 * Transparent pixels have alpha=0; glyph pixels have alpha=255.
 */
static uint8_t *render_text_rgba(const char *text,
                                  uint32_t *out_w, uint32_t *out_h)
{
    int cols  = measure_cols(text);
    int lines = count_lines(text);

    uint32_t glyph_w = (uint32_t)OT_GLYPH_W;
    uint32_t glyph_h = (uint32_t)OT_GLYPH_H;
    uint32_t pad     = (uint32_t)OT_PADDING;

    uint32_t w = pad * 2u + (uint32_t)cols  * glyph_w;
    uint32_t h = pad * 2u + (uint32_t)lines * glyph_h;

    uint8_t *buf = calloc((size_t)w * h, 4u);
    if (!buf)
        return NULL;

    int cx = (int)pad;
    int cy = (int)pad;

    for (const char *p = text; *p; p++) {
        if (*p == '\n') {
            cx  = (int)pad;
            cy += (int)glyph_h;
            continue;
        }

        unsigned ch = (unsigned char)*p;
        if (ch < 0x20u || ch > 0x7eu)
            ch = '?';

        const uint8_t *glyph = g_font[ch - 0x20u];

        for (uint32_t row = 0u; row < glyph_h; row++) {
            uint8_t bits = glyph[row];

            for (uint32_t col = 0u; col < glyph_w; col++) {
                if (bits & (0x80u >> col)) {
                    uint32_t px = (uint32_t)cx + col;
                    uint32_t py = (uint32_t)cy + row;
                    uint8_t *pixel = buf + (py * w + px) * 4u;
                    pixel[0] = 0xff; /* R */
                    pixel[1] = 0xff; /* G */
                    pixel[2] = 0xff; /* B */
                    pixel[3] = 0xff; /* A */
                }
            }
        }

        cx += (int)glyph_w;
    }

    *out_w = w;
    *out_h = h;
    return buf;
}

/*
 * Integer upscale of a src RGBA buffer (src_w×src_h) by factor `scale`.
 * Returns a newly malloc'd buffer of size (src_w*scale × src_h*scale) × 4.
 */
static uint8_t *upscale_rgba(const uint8_t *src,
                               uint32_t src_w, uint32_t src_h,
                               uint32_t scale)
{
    uint32_t dst_w = src_w * scale;
    uint32_t dst_h = src_h * scale;
    uint8_t *dst   = malloc((size_t)dst_w * dst_h * 4u);

    if (!dst)
        return NULL;

    for (uint32_t sy = 0u; sy < src_h; sy++) {
        for (uint32_t sx = 0u; sx < src_w; sx++) {
            const uint8_t *sp = src + (sy * src_w + sx) * 4u;

            for (uint32_t dy = 0u; dy < scale; dy++) {
                for (uint32_t dx = 0u; dx < scale; dx++) {
                    uint32_t dpx = sx * scale + dx;
                    uint32_t dpy = sy * scale + dy;
                    uint8_t *dp  = dst + (dpy * dst_w + dpx) * 4u;
                    dp[0] = sp[0];
                    dp[1] = sp[1];
                    dp[2] = sp[2];
                    dp[3] = sp[3];
                }
            }
        }
    }

    return dst;
}

/* ----------------------------------------------------------------
 * Public API
 * ---------------------------------------------------------------- */

void overlay_text_update(struct overlay_text *ot, const char *text)
{
    if (!ot || !text)
        return;

    if (ot->initialized && strncmp(ot->last_text, text, OT_MAX_TEXT - 1u) == 0)
        return;

    /* Destroy old texture first. */
    if (ot->tex) {
        glDeleteTextures(1, &ot->tex);
        ot->tex = 0;
    }

    uint32_t base_w = 0;
    uint32_t base_h = 0;
    uint8_t *base   = render_text_rgba(text, &base_w, &base_h);

    if (!base)
        return;

    uint8_t *pixels = upscale_rgba(base, base_w, base_h, OT_SCALE);
    free(base);
    if (!pixels)
        return;

    uint32_t final_w = base_w * (uint32_t)OT_SCALE;
    uint32_t final_h = base_h * (uint32_t)OT_SCALE;

    glGenTextures(1, &ot->tex);
    glBindTexture(GL_TEXTURE_2D, ot->tex);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA,
                 (GLsizei)final_w, (GLsizei)final_h,
                 0, GL_RGBA, GL_UNSIGNED_BYTE, pixels);
    glBindTexture(GL_TEXTURE_2D, 0);
    display_renderer_texture_mipmap_trilinear(ot->tex);

    free(pixels);

    ot->tex_w = final_w;
    ot->tex_h = final_h;
    snprintf(ot->last_text, OT_MAX_TEXT, "%s", text);
    ot->initialized = true;
}

void overlay_text_destroy(struct overlay_text *ot)
{
    if (!ot)
        return;
    if (ot->tex)
        glDeleteTextures(1, &ot->tex);
    memset(ot, 0, sizeof(*ot));
}
